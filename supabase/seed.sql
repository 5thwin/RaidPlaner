-- - min_item_levels(난이도별 입장 최소 아이템 레벨): 1막~4막/종막:카제로스/세르카/
--   지평의 성당은 사용자가 난이도별 정확한 값을 확인해줘서(2026-07-11) 반영했다.
--   서막: 에키드나는 아직 예전 프로토타입 추정치(난이도 구분 없이 동일 값)라
--   TODO: 확인 필요 상태로 남아있다.
-- - "서막: 에키드나"는 실제 정식 명칭/난이도 구성/인원수가 패치마다 바뀌었을 수 있어
--   확신도가 낮다. 반드시 실제 게임 정보와 대조해서 수정해야 한다.
--
-- 아래 insert 목록은 출시일 순으로 정렬해뒀다(가독성 목적이자 display_order 값 순서).
