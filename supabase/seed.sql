-- raid_types 테이블에 실제 로스트아크 레이드 목록을 채워 넣는다.
-- 이 스크립트는 마이그레이션 파일(supabase/migrations/*.sql)들을 전부 실행한 "다음"에 실행한다.
--
-- 여러 번 실행해도 안전하도록(idempotent) `on conflict (name) do nothing`을 사용했다.
-- 즉, 이미 같은 이름의 레이드가 있으면 새로 추가하지 않고 조용히 건너뛴다.
-- 값을 고치고 싶으면 이 스크립트를 다시 실행하지 말고, SQL Editor에서 직접
-- update문으로 해당 row를 수정하는 것을 권장한다.
-- (이미 seed.sql을 한 번 실행해서 row가 존재하는 DB라면, 아래 min_item_levels 값을
-- 반영하기 위해 supabase/update_min_item_levels.sql을 대신 실행할 것.)
--
