-- 베히모스는 실제로는 16인 레이드인데, raid_types.max_players는 4/8만 허용하는
-- check 제약이 있어서 표현할 수 없다. 별도로 16인을 지원하도록 스키마를 바꾸기보다
-- 목록에서 빼기로 했다(사용자 확인).
--
-- 주의: 이 레이드로 이미 생성된 파티가 있으면 외래키 제약 때문에 삭제가 실패한다
-- (parties.raid_type_id에 on delete cascade가 없음 — 의도적으로 그렇게 만들어서
-- 실제 사용 중인 레이드가 실수로 삭제되는 걸 막는다). 삭제가 실패하면 그 레이드로
-- 만들어진 파티를 먼저 지울지 확인한 뒤 다시 시도할 것.
delete from public.raid_types
where name = '베히모스';
